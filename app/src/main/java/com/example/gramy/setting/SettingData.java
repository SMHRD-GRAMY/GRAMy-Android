package com.example.gramy.setting;

public class SettingData {

    private String menu;
    private int imgId;

    public SettingData(){}

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }
}